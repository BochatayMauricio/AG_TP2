class producto:
    pass
    def __init__(self,numero,valor,volumen):
        self.numero = numero
        self.valor = valor
        self.volumen = volumen
    
    def getValor(self):
        return self.valor
    
    def getVolumen(self):
        return self.volumen
    
    def getNumero(self):
        return self.numero
    
